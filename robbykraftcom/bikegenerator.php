<h1>bicycle wheel-mounted generator</h1>

<p>This was a successful attempt to make a cheap batteryless bicycle light from recycled parts. The result was impressive and was in active use for years.</p>

<p>The generator was hand-wound magnet wire harvested from computer case fans and strong magets attached to the wheel spokes. The brightness of the light was softened with a large capacitor and the result was a lamp with a softly pulsating glow at a frequency relative to the speed of the bike.</p>

