<h1>Snowflake formation study</h1>

<p>This project is an observation based study on snowflake growth, making heavy use of the photography work of Kenneth Libbrecht and Wilson Bently. An binary tree based algorithm was developed to approximate the 2d form of snowflakes by growing 60 degree hexagonal polygons of varying edge lengths.</p>

