<h1>CAZA at the Venice Biennale</h1>

<p>The release of <a href="//cazarch.com/">CAZA</a>'s book was commemorated by an installation at the Venice Biennale in 2016. This interactive mirror waits for people to approach and when it detects someone in front if it shapes taken from the architectural idioms at CAZA begin to appear and decorate the viewer's face. Collaboration with Zach Lieberman.</p>

<p>Our role was to incorporate the visual language at CAZA in a fun interactive installation.</p>

