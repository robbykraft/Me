<h1>Target Wonderland</h1>

<p>In 2015 Target created a wild landscape in a building in Chelsea, NYC gathering many types of creators together. I was in charge of the software running a jumbo Etch-a-Sketch.</p>

<p>I got a proprietary Etch-a-Sketch rendering library working in OpenFrameworks and managed the communication of taking and transmitting photos between 2 computers and the experience of animating people's faces on the large Etch-a-Sketch screen.</p>

