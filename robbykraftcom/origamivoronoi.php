<p>This is an algorithm to generate origami crease patterns from Voronoi diagrams that contain foldable gussets of widths proportional to the distance between neighboring cell sites. Further work is needed to resolve the intersections between gussets to make the folded form lie orthogonal to the Voronoi plane for all possible Voronoi diagrams.</p>

